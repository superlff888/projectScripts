# 构造方法
a = '123'
print(tuple(a))

# 解包

tuple_ = [1, 2, 3]
a, b, c = tuple_  # 解包分别赋值
print(a, b, c)

print(type(*tuple_))
print(f"a,b,c为：{a, b, c}")


# 带 * 号的解包

a1 = (1, 3, 4)
b1 = (3, 3, 4)
c = [*a1, *b1]  # [1, 3, 4, 3, 3, 4]
d = (*a1, *b1)  # (1, 3, 4, 3, 3, 4)
print(c)
print(d)
