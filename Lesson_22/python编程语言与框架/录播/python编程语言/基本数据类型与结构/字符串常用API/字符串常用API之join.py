# join列表、元组转化为字符串:将列表中的字符串取出来,并放到""中组成一个新的字符串

a = ["i", "p", "h", "o", "n", "e", "1", "3"]
b = ("i", "p", "h", "o", "n", "e", "1", "5")
print("".join(a))
print("|".join(a))
print(" ".join(a))
print(" ".join(b))
